# Faça um código que lê duas entradas de números e tira uma média entre eles. 
primeiro_numero = int(input("Digite um numero: "))
segundo_numero = int(input("Digite outro numero: "))

media = (primeiro_numero*segundo_numero) / 2

print(f"A media dos numero : {primeiro_numero:.1f} e {segundo_numero:.1f}. Resulta em : {media:.1f} centrimetros quadrados")