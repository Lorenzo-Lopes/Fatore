#Faça um programa que lê duas entradas de números e calcula a área de um triângulo retângulo. 

base = int(input("Digite em cm o tamanha da base do triangulo: "))
altura = int(input("Digite em cm a altura do triangulo: "))

area = (base*altura)/2

print(f"Em um triangulo de retangulo de base: {base} e altura:{altura} tema uma area de {area}")