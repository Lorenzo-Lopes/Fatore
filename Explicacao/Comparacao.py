"""
Operadores de comparação (relacionais)
OP      Significado         Exemplo (True)
>       maior               2 > 1
>=      maior ou igual      2 >= 2
<       menor               1 < 2
<=      menor ou igual      2 <= 2
==      igual               'a' == 'a'
!=      diferente           'a' != 'b'
"""
maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(f"2 é maior que 1? {maior}\n")
print(f"2 é maior ou igual a 2? {maior_ou_igual}\n")
print(f"1 é menor que 2? {menor}\n")
print(f"2 é menor ou igual a 2? {menor_ou_igual}\n")
print(f"'a' é igual a 'a'? {igual}\n")
print(f"'a' é diferente de 'b'? {diferente}\n")